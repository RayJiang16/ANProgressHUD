import UIKit

extension UIFont {
    
    static var label: UIFont {
        return UIFont.systemFont(ofSize: 16, weight: .bold)
    }
    
    static var detailLabel: UIFont {
        return UIFont.systemFont(ofSize: 12, weight: .bold)
    }
}
